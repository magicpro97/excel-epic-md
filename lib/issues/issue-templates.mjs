/**
 * @module issue-templates
 * Renders GitHub Issue body from synthesis data, optimized for Copilot coding agent.
 */

/** GitHub issue body max length (65536 chars) with safety margin */
const MAX_BODY_LENGTH = 60000;

/**
 * Render issue body for a task-level issue
 * @param {object} task - Task object from synthesis
 * @param {Array<object>} relatedReqs - Related requirement objects
 * @param {Array<object>} relatedTables - Related specification tables
 * @param {Array<string>} acceptanceCriteria - Acceptance criteria list
 * @param {object} epic - Epic metadata
 * @param {object} context - Context object (background, objectives, scope)
 * @returns {string} Markdown body for GitHub issue
 */
export function renderTaskIssueBody(task, relatedReqs, relatedTables, acceptanceCriteria, epic, context) {
  const sections = [];

  // Problem Statement
  sections.push(`## Problem Statement\n\n${task.description}`);

  // Context
  if (epic?.summary) {
    sections.push(`## Context\n\n> ${epic.summary}`);
  }

  if (context?.background) {
    sections.push(`### Background\n\n${context.background}`);
  }

  // Requirements
  if (relatedReqs.length > 0) {
    const reqLines = relatedReqs.map((r) => {
      const priorityBadge = r.priority ? ` \`${r.priority.toUpperCase()}\`` : '';
      return `- **${r.id}**${priorityBadge}: ${r.description}`;
    });
    sections.push(`## Requirements\n\n${reqLines.join('\n')}`);
  }

  // Acceptance Criteria
  if (acceptanceCriteria && acceptanceCriteria.length > 0) {
    const acLines = acceptanceCriteria.map((ac) => `- [ ] ${ac}`);
    sections.push(`## Acceptance Criteria\n\n${acLines.join('\n')}`);
  }

  // Specification Tables — add incrementally to stay within size limit
  if (relatedTables.length > 0) {
    const currentLength = sections.join('\n\n').length;
    const budget = MAX_BODY_LENGTH - currentLength - 500; // reserve for footer
    if (budget > 500) {
      const tableBlocks = [];
      let used = 0;
      for (const t of relatedTables) {
        const notes = t.notes ? `\n\n> ${t.notes}` : '';
        const block = `### ${t.title}\n\n${t.markdownTable}${notes}`;
        if (used + block.length > budget) {
          tableBlocks.push(`\n\n> ⚠️ ${relatedTables.length - tableBlocks.length} more table(s) omitted due to size limit. See epic_synthesis.json for full data.`);
          break;
        }
        tableBlocks.push(block);
        used += block.length;
      }
      sections.push(`## Specification Tables\n\n${tableBlocks.join('\n\n---\n\n')}`);
    } else {
      sections.push(`## Specification Tables\n\n> ⚠️ ${relatedTables.length} table(s) omitted due to size limit. See epic_synthesis.json for full data.`);
    }
  }

  // Footer
  sections.push(
    `---\n\n_Auto-generated by [excel-epic-md](https://github.com/magicpro97/excel-epic-md) from Epic: **${epic?.title || 'Unknown'}**_`,
  );

  return sections.join('\n\n');
}

/**
 * Render issue body for the parent epic issue
 * @param {object} synthesis - Full synthesis object
 * @returns {string} Markdown body for the epic issue
 */
export function renderEpicIssueBody(synthesis) {
  const epic = synthesis.epic || {};
  const context = synthesis.context || {};
  const requirements = synthesis.requirements || [];
  const tasks = synthesis.tasks || [];
  const openQuestions = synthesis.openQuestions || [];
  const assumptions = synthesis.assumptions || [];

  const sections = [];

  // Summary
  sections.push(`## Summary\n\n${epic.summary || 'N/A'}`);

  // Context
  if (context.background) {
    sections.push(`## Background\n\n${context.background}`);
  }
  if (context.objectives?.length > 0) {
    sections.push(`### Objectives\n\n${context.objectives.map((o) => `- ${o}`).join('\n')}`);
  }
  if (context.scope) {
    sections.push(`### Scope\n\n${context.scope}`);
  }

  // Tasks (will become child issues)
  if (tasks.length > 0) {
    const taskLines = tasks.map((t) => `- [ ] **${t.id}**: ${t.description}`);
    sections.push(`## Tasks\n\n${taskLines.join('\n')}`);
  }

  // Requirements summary
  const catCounts = {};
  for (const r of requirements) {
    const cat = r.category || 'other';
    catCounts[cat] = (catCounts[cat] || 0) + 1;
  }
  const catSummary = Object.entries(catCounts)
    .map(([cat, count]) => `- ${cat}: ${count}`)
    .join('\n');
  sections.push(`## Requirements Summary\n\nTotal: **${requirements.length}** requirements\n\n${catSummary}`);

  // Open Questions
  if (openQuestions.length > 0) {
    const qLines = openQuestions.map((q) => {
      if (typeof q === 'string') return `- ${q}`;
      return `- **${q.question}**${q.context ? `\n  - ${q.context}` : ''}`;
    });
    sections.push(`## Open Questions\n\n${qLines.join('\n')}`);
  }

  // Assumptions
  if (assumptions.length > 0) {
    sections.push(`## Assumptions\n\n${assumptions.map((a) => `- ${a}`).join('\n')}`);
  }

  // Footer
  sections.push(
    `---\n\n_Auto-generated by [excel-epic-md](https://github.com/magicpro97/excel-epic-md)_`,
  );

  return sections.join('\n\n');
}
